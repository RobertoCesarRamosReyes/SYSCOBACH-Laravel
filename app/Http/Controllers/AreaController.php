     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $area=new Area;
        $area->estante=$request->estante;
        $area->charola=$request->charola;
        $area->save();
        return back()->with('agregar_ubicacion','La ubiación se ha agregado exitosamente');
    }

